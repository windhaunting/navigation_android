public class PlanePoint {

	private double planex;
	private double planey;
	
	public PlanePoint(double planex, double planey)
	{
		this.planex = planex;
		this.planey = planey;
	}
	
	public double getPlanex()
	{
		return this.planex;
	}
	
	public double getPlaney()
	{
		return this.planey;
	}
	
}
